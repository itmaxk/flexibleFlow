def log_event(level, message, with_traceback=False):
    try:
        ts = datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%SZ")
        line = f"{ts} [{level}] {message}\n"
        with open(LOG_PATH, "a", encoding="utf-8") as f:
            f.write(line)
            if with_traceback:
                f.write(traceback.format_exc())
                f.write("\n")
    except OSError:
        pass


def run(cmd, desc=None, stream=False):
    if desc:
        print(f"{Colors.CYAN}[...]{Colors.END} {desc}")
    cmd_str = " ".join(shlex.quote(x) for x in cmd)
    log_event("INFO", f"run: {cmd_str}")
    try:
        if stream:
            subprocess.run(cmd, check=True, text=True)
        else:
            subprocess.run(cmd, check=True, capture_output=True, text=True)
        return True
    except subprocess.CalledProcessError as e:
        err = (e.stderr or e.stdout or "").strip()
        print(f"{Colors.RED}[Р В Р’В Р РЋРІР‚С”Р В Р Р‹Р Р†РІР‚С™Р’В¬Р В Р’В Р РЋРІР‚ВР В Р’В Р вЂ™Р’В±Р В Р’В Р РЋРІР‚СњР В Р’В Р вЂ™Р’В°]{Colors.END} {err}")
        log_event("ERROR", f"run failed ({e.returncode}): {cmd_str}; {err}")
        return False
